import assert from "node:assert/strict";
import { readFile, writeFile } from "node:fs/promises";
import * as dagCbor from "@ipld/dag-cbor";
import { CID } from "multiformats/cid";
import { sha256 } from "multiformats/hashes/sha2";

const parameterUrl = new URL(
  "../../../compatibility/governance-day-parameters.json",
  import.meta.url,
);
const generatedUrl = new URL("../assembly/generated_parameters.ts", import.meta.url);
const parameters = JSON.parse(await readFile(parameterUrl, "utf8"));

assert.equal(parameters.schemaVersion, 1);
assert.ok(parameters.normal);
assert.ok(parameters.critical);
assert.ok(parameters.identityMath.statusBps.Human > parameters.identityMath.statusBps.Verified);
assert.ok(parameters.identityMath.statusBps.Verified > parameters.identityMath.statusBps.Newbie);
assert.equal(
  parameters.rejectedBondBurnBps + parameters.rejectedBondTreasuryBps,
  10_000,
);
assert.equal(parameters.lifecycle.executionWindowBlocks, parameters.schedule.executionWindowBlocks);
for (const gate of [parameters.normal, parameters.critical]) {
  assert.equal(gate.minimumAiIndependenceGroups, gate.minimumAiOwnerIdentities);
  assert.ok(gate.criticalFindingOwnerThreshold >= 2);
  assert.ok(gate.criticalFindingOwnerThreshold <= gate.minimumAiOwnerIdentities);
  assert.ok(gate.yesThresholdBps <= 10_000);
  assert.ok(gate.turnoutQuorumBps <= 10_000);
}

const encoded = dagCbor.encode(parameters);
const parameterCid = CID.createV1(dagCbor.code, await sha256.digest(encoded)).toString();
const quote = (value) => JSON.stringify(String(value));
const number = (value) => {
  assert.equal(Number.isSafeInteger(value), true);
  assert.equal(value >= 0, true);
  return String(value);
};
const bool = (value) => {
  assert.equal(typeof value, "boolean");
  return String(value);
};
const line = (name, type, value) => `export const ${name}: ${type} = ${value};`;
const gateLines = (prefix, gate) => [
  line(`${prefix}_TURNOUT_QUORUM_BPS`, "u16", number(gate.turnoutQuorumBps)),
  line(`${prefix}_YES_THRESHOLD_BPS`, "u16", number(gate.yesThresholdBps)),
  line(`${prefix}_MIN_PARTICIPATING_IDENTITIES`, "u32", number(gate.minimumParticipatingIdentities)),
  line(`${prefix}_MIN_YES_IDENTITIES`, "u32", number(gate.minimumYesIdentities)),
  line(`${prefix}_MIN_STRONG_YES`, "u32", number(gate.minimumVerifiedOrHumanYes)),
  line(`${prefix}_MIN_AI_ATTESTATIONS`, "u32", number(gate.minimumAiAttestations)),
  line(`${prefix}_MIN_AI_RUNTIME_GROUPS`, "u32", number(gate.minimumAiIndependenceGroups)),
  line(`${prefix}_MIN_AI_FAMILIES`, "u32", number(gate.minimumAiFamilies)),
  line(`${prefix}_MIN_AI_OWNER_IDENTITIES`, "u32", number(gate.minimumAiOwnerIdentities)),
  line(`${prefix}_CRITICAL_FINDING_OWNER_THRESHOLD`, "u32", number(gate.criticalFindingOwnerThreshold)),
  line(`${prefix}_MIN_BUILDERS`, "u32", number(gate.minimumBuilders)),
  line(`${prefix}_MIN_BUILDER_PLATFORMS`, "u32", number(gate.minimumBuilderPlatforms)),
  line(`${prefix}_MIN_AVAILABILITY_PROVIDERS`, "u32", number(gate.minimumDataAvailabilityProviders)),
  line(`${prefix}_REQUIRE_MATCHING_BUILD_DIGESTS`, "bool", bool(gate.requireMatchingBuildDigests)),
];
const limitLines = (prefix, limits) => [
  line(`${prefix}_MAX_AFFECTED_REPOSITORIES`, "u32", number(limits.maxAffectedRepositories)),
  line(`${prefix}_MAX_CHANGED_FILES`, "u32", number(limits.maxChangedFiles)),
  line(`${prefix}_MAX_PATCH_BYTES`, "u64", number(limits.maxPatchBytes)),
  line(`${prefix}_MAX_SOURCE_PACKAGE_BYTES`, "u64", number(limits.maxSourcePackageBytes)),
  line(`${prefix}_MAX_DESCRIPTION_BYTES`, "u32", number(limits.maxDescriptionBytes)),
  line(`${prefix}_MAX_MIGRATION_OPERATIONS`, "u32", number(limits.maxMigrationOperations)),
];

const math = parameters.identityMath;
const lifecycle = parameters.lifecycle;
const bonds = parameters.contractBondPolicy;
const resources = parameters.contractResourceLimits;
const schedule = parameters.schedule;
const output = [
  "// Generated by scripts/generate-parameters.mjs. Do not edit by hand.",
  "// The build fails closed when this file differs from the canonical parameter JSON.",
  line("GOVERNANCE_PARAMETER_SET_CID", "string", quote(parameterCid)),
  line("GOVERNANCE_PARAMETER_PROFILE_JSON", "string", JSON.stringify(JSON.stringify(parameters))),
  line("GOV_STAKE_QUANTUM_ATOMS", "u64", quote(parameters.stakeQuantumAtoms).slice(1, -1)),
  line("GOV_MIN_ACTIVE_STAKE_ATOMS", "string", quote(parameters.minimumActiveStakeAtoms)),
  line("GOV_IDNA_ATOMS_PER_UNIT", "string", quote(math.idnaAtomsPerUnit)),
  line("GOV_STATUS_HUMAN_BPS", "u16", number(math.statusBps.Human)),
  line("GOV_STATUS_VERIFIED_BPS", "u16", number(math.statusBps.Verified)),
  line("GOV_STATUS_NEWBIE_BPS", "u16", number(math.statusBps.Newbie)),
  line("GOV_FLIP_PRIOR_REPORTED", "u64", number(math.flipPriorReported)),
  line("GOV_FLIP_PRIOR_TOTAL", "u64", number(math.flipPriorTotal)),
  line("GOV_FLIP_TRUST_FLOOR_BPS", "u16", number(math.flipTrustFloorBps)),
  line("GOV_FLIP_TRUST_CEILING_BPS", "u16", number(math.flipTrustCeilingBps)),
  line("GOV_FLIP_PENALTY_SCALE", "u64", number(math.flipPenaltyScale)),
  line("GOV_REVIEW_PERIOD_BLOCKS", "u64", number(lifecycle.reviewPeriodBlocks)),
  line("GOV_VOTING_PERIOD_BLOCKS", "u64", number(lifecycle.votingPeriodBlocks)),
  line("GOV_CHALLENGE_PERIOD_BLOCKS", "u64", number(lifecycle.challengePeriodBlocks)),
  line("GOV_TIMELOCK_BLOCKS", "u64", number(lifecycle.timelockBlocks)),
  line("GOV_EXECUTION_WINDOW_BLOCKS", "u64", number(lifecycle.executionWindowBlocks)),
  line("GOV_UNBONDING_DELAY_EPOCHS", "u16", number(lifecycle.unbondingDelayEpochs)),
  line("GOV_MIN_IDENTITY_METRICS_ATTESTATIONS", "u32", number(lifecycle.minimumIdentityMetricsAttestations)),
  line("GOV_MIN_REVIEWER_BOND_ATOMS", "string", quote(bonds.minimumReviewerBondAtoms)),
  line("GOV_MIN_BUILDER_BOND_ATOMS", "string", quote(bonds.minimumBuilderBondAtoms)),
  line("GOV_MIN_AVAILABILITY_BOND_ATOMS", "string", quote(bonds.minimumDataAvailabilityBondAtoms)),
  line("GOV_STALE_PROCESSING_FEE_ATOMS", "string", quote(bonds.staleProcessingFeeAtoms)),
  line("GOV_REJECTED_PROPOSAL_RETURN_BPS", "u16", number(bonds.rejectedProposalReturnBps)),
  line("GOV_EXPIRED_REVIEW_RETURN_BPS", "u16", number(bonds.expiredReviewRoundReturnBps)),
  line("GOV_EXPIRED_PROPOSAL_RETURN_BPS", "u16", number(bonds.expiredProposalReturnBps)),
  line("GOV_SUCCESSFUL_CHALLENGE_PROPOSER_RETURN_BPS", "u16", number(bonds.successfulChallengeProposerReturnBps)),
  line("GOV_UNAVAILABLE_ATTESTATION_SLASH_BPS", "u16", number(bonds.unavailableAttestationSlashBps)),
  line("GOV_FRAUDULENT_ACTOR_STAKE_SLASH_BPS", "u16", number(bonds.fraudulentActorStakeSlashBps)),
  line("GOV_MAX_STAKE_HISTORY", "u32", number(resources.maxStakeHistory)),
  line("GOV_MAX_PORTABLE_ARTIFACT_SIZE", "u64", number(resources.maxPortableArtifactSize)),
  line("GOV_MAX_COMMITTED_ATTESTATIONS", "u32", number(resources.maxCommittedAttestations)),
  line("GOV_MAX_ATTESTATIONS_PER_OWNER_PER_CLASS", "u32", number(resources.maxAttestationsPerOwnerPerClass)),
  line("GOV_MAX_REQUIRED_AVAILABILITY_CIDS", "u32", number(resources.maxRequiredAvailabilityCids)),
  line("GOV_MAX_SCOPE_PROOF_BYTES", "u64", number(resources.maxScopeProofBytes)),
  line("GOV_MAX_SCOPE_FILES_PER_REPOSITORY", "u32", number(resources.maxScopeFilesPerRepository)),
  line("GOV_MAX_SCOPE_SOURCE_FILE_BYTES", "u64", number(resources.maxScopeSourceFileBytes)),
  line("GOV_MAX_SCOPE_SOURCE_TREE_BYTES", "u64", number(resources.maxScopeSourceTreeBytes)),
  line("GOV_NORMAL_PROPOSAL_BOND_ATOMS", "string", quote(parameters.normalProposalBondAtoms)),
  line("GOV_CRITICAL_PROPOSAL_BOND_ATOMS", "string", quote(parameters.criticalProposalBondAtoms)),
  line("GOV_REJECTED_BOND_BURN_BPS", "u16", number(parameters.rejectedBondBurnBps)),
  line("GOV_REJECTED_BOND_TREASURY_BPS", "u16", number(parameters.rejectedBondTreasuryBps)),
  line("GOV_NO_QUORUM_FEE_ATOMS", "string", quote(parameters.noQuorumFeeAtoms)),
  line("GOV_CANCELLATION_FEE_ATOMS", "string", quote(parameters.cancellationFeeAtoms)),
  line("GOV_MAX_PROPOSALS_PER_EPOCH", "u32", number(parameters.maximumProposalsPerEpoch)),
  line("GOV_PROPOSAL_CUTOFF_OFFSET", "u64", number(schedule.proposalCutoff)),
  line("GOV_COMMIT_START_OFFSET", "u64", number(schedule.commitStart)),
  line("GOV_COMMIT_END_OFFSET", "u64", number(schedule.commitEnd)),
  line("GOV_REVEAL_END_OFFSET", "u64", number(schedule.revealEnd)),
  line("GOV_NORMAL_GRACE_BLOCKS", "u64", number(schedule.normalGraceBlocks)),
  line("GOV_CRITICAL_GRACE_BLOCKS", "u64", number(schedule.criticalGraceBlocks)),
  line("GOV_EPOCH_EXECUTION_WINDOW_BLOCKS", "u64", number(schedule.executionWindowBlocks)),
  line("GOV_OBSERVATION_WINDOW_BLOCKS", "u64", number(schedule.observationWindowBlocks)),
  ...limitLines("GOV_NORMAL", parameters.normalLimits),
  ...limitLines("GOV_CRITICAL", parameters.criticalLimits),
  ...gateLines("GOV_NORMAL", parameters.normal),
  ...gateLines("GOV_CRITICAL", parameters.critical),
  "",
].join("\n");

if (process.argv.includes("--check")) {
  const existing = await readFile(generatedUrl, "utf8").catch(() => "");
  if (existing !== output) {
    throw new Error("generated_parameters.ts is stale; run pnpm generate:parameters");
  }
} else {
  await writeFile(generatedUrl, output, "utf8");
}
